def highlight_keyword(text: str, keyword: str) -> str:
    if not text:
        return ""
    safe_text = html.escape(text)
    if not keyword:
        return safe_text.replace("\n", "<br>")
    safe_kw = html.escape(keyword)
    pattern = re.compile(re.escape(safe_kw), re.IGNORECASE)
    highlighted = pattern.sub(
        lambda m: f"<mark style='background-color:#ffeb3b;font-weight:bold;padding:0 3px;'>{m.group()}</mark>",
        safe_text
    )
    return highlighted.replace("\n", "<br>")

def keyword_in_text(text: str, keyword: str) -> bool:
    if not keyword:
        return False
    return bool(re.search(re.escape(keyword), text, re.IGNORECASE))

